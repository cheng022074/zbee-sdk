
/**
 * 
 * 得到对应代码的值键值 
 * 
 * @param {number} code 键代码
 * 
 * @return {mixed} 键值 
 * 
 */

 switch(code){

    case 39:

        return 'DIRECTION::RIGHT' ;

    case 37:

        return 'DIRECTION::LEFT' ;

    case 38:

        return 'DIRECTION::UP' ;

    case 40:

        return 'DIRECTION::DOWN' ;
 }